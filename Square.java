public class Square {

    int x;
    int y;

    public Square(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }

    public void setX(int newx) {
        this.x = newx;
    }

    public void setY(int newy) {
        this.y = newy;
    }

}
